package com.clothes.pojo;

import java.util.List;

/**
 * 物品出入库记录
 * @author 项目组
 *
 */
public class Record {
	//id
    private String reId;
    //类型
    private Integer reType;
    //仓库
    private Store store;
    //员工
    private Employee employee;
    //出入库时间
    private String reDate;
    //备注
    private String remark;
    //状态
    private Integer reState;
    //出入库明细
    private List<Recorditem> recorditemList;

    public String getReId() {
        return reId;
    }

    public void setReId(String reId) {
        this.reId = reId;
    }

    public Integer getReType() {
        return reType;
    }

    public void setReType(Integer reType) {
        this.reType = reType;
    }
    
    public void setReDate(String reDate) {
        this.reDate = reDate;
    }

    public String getRemark() {
        return remark;
    }

    public void setRemark(String remark) {
        this.remark = remark;
    }

    public Integer getReState() {
        return reState;
    }

    public void setReState(Integer reState) {
        this.reState = reState;
    }

	public Store getStore() {
		return store;
	}

	public void setStore(Store store) {
		this.store = store;
	}

	public Employee getEmployee() {
		return employee;
	}

	public void setEmployee(Employee employee) {
		this.employee = employee;
	}

	public String getReDate() {
		return reDate;
	}

	public List<Recorditem> getRecorditemList() {
		return recorditemList;
	}

	public void setRecorditemList(List<Recorditem> recorditemList) {
		this.recorditemList = recorditemList;
	}

	@Override
	public String toString() {
		return "Record [reId=" + reId + ", reType=" + reType + ", store=" + store + ", employee=" + employee
				+ ", reDate=" + reDate + ", remark=" + remark + ", reState=" + reState + ", recorditemList="
				+ recorditemList + "]";
	}
    
}
